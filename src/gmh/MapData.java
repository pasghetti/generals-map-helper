package gmh;

public class MapData {
	String title;
	String description;
	int width;
	int height;
	String map;
	String user_id;
	public MapData(String t, String desc, int w, int h, String m, String uid) {
		title = t;
		description = desc;
		width = w;
		height = h;
		map = m;
		user_id = uid;
	}
}
